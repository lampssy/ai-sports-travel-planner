from __future__ import annotations

import os
from pathlib import Path
from typing import Any

import psycopg
from psycopg.rows import dict_row

from app.config.env import load_dotenv_file
from app.data.loader import DEFAULT_RESORTS_PATH, load_resorts_from_path

DATABASE_URL_ENV_VAR = "DATABASE_URL"
DEFAULT_DATABASE_URL = (
    "postgresql://planner:planner@127.0.0.1:5432/ai_sports_travel_planner"
)


def resolve_database_url() -> str:
    load_dotenv_file()
    return os.getenv(DATABASE_URL_ENV_VAR, DEFAULT_DATABASE_URL)


def connect(
    database_url: str | None = None,
) -> psycopg.Connection[Any]:
    return psycopg.connect(database_url or resolve_database_url(), row_factory=dict_row)


def bootstrap_database(
    database_url: str | None = None,
    *,
    resorts_path: Path = DEFAULT_RESORTS_PATH,
) -> None:
    with connect(database_url) as connection:
        _create_schema(connection)
        _sync_resorts_from_seed(connection, resorts_path)
        _clear_legacy_seeded_conditions(connection)


def reset_database(
    database_url: str | None = None,
    *,
    resorts_path: Path = DEFAULT_RESORTS_PATH,
) -> None:
    with connect(database_url) as connection:
        connection.execute("DROP SCHEMA IF EXISTS public CASCADE")
        connection.execute("CREATE SCHEMA public")
        _create_schema(connection)
        _sync_resorts_from_seed(connection, resorts_path)
        _clear_legacy_seeded_conditions(connection)


def _create_schema(connection: psycopg.Connection[Any]) -> None:
    connection.execute(
        """
        CREATE TABLE IF NOT EXISTS resorts (
            resort_id TEXT PRIMARY KEY,
            name TEXT NOT NULL UNIQUE,
            country TEXT NOT NULL,
            region TEXT NOT NULL,
            price_level TEXT NOT NULL,
            latitude DOUBLE PRECISION NOT NULL,
            longitude DOUBLE PRECISION NOT NULL,
            base_elevation_m INTEGER NOT NULL,
            summit_elevation_m INTEGER NOT NULL,
            season_start_month INTEGER NOT NULL,
            season_end_month INTEGER NOT NULL
        );

        CREATE TABLE IF NOT EXISTS ski_areas (
            id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            resort_id TEXT NOT NULL REFERENCES resorts(resort_id) ON DELETE CASCADE,
            ski_area_id TEXT NOT NULL UNIQUE,
            name TEXT NOT NULL,
            latitude DOUBLE PRECISION NOT NULL,
            longitude DOUBLE PRECISION NOT NULL,
            base_elevation_m INTEGER NOT NULL,
            summit_elevation_m INTEGER NOT NULL,
            season_start_month INTEGER NOT NULL,
            season_end_month INTEGER NOT NULL
        );

        CREATE TABLE IF NOT EXISTS stay_bases (
            id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            resort_id TEXT NOT NULL REFERENCES resorts(resort_id) ON DELETE CASCADE,
            name TEXT NOT NULL,
            price_range TEXT NOT NULL,
            price_min DOUBLE PRECISION NOT NULL,
            price_max DOUBLE PRECISION NOT NULL,
            quality TEXT NOT NULL,
            lift_distance TEXT NOT NULL
        );

        CREATE TABLE IF NOT EXISTS stay_base_skill_levels (
            stay_base_id INTEGER NOT NULL REFERENCES stay_bases(id) ON DELETE CASCADE,
            skill_level TEXT NOT NULL,
            PRIMARY KEY (stay_base_id, skill_level)
        );

        CREATE TABLE IF NOT EXISTS rentals (
            id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            resort_id TEXT NOT NULL REFERENCES resorts(resort_id) ON DELETE CASCADE,
            name TEXT NOT NULL,
            price_range TEXT NOT NULL,
            price_min DOUBLE PRECISION NOT NULL,
            price_max DOUBLE PRECISION NOT NULL,
            quality TEXT NOT NULL,
            lift_distance TEXT NOT NULL
        );

        CREATE TABLE IF NOT EXISTS resort_conditions (
            resort_id TEXT PRIMARY KEY
                REFERENCES ski_areas(ski_area_id) ON DELETE CASCADE,
            resort_name TEXT NOT NULL UNIQUE,
            snow_confidence_score DOUBLE PRECISION NOT NULL,
            snow_confidence_label TEXT NOT NULL,
            availability_status TEXT NOT NULL,
            weather_summary TEXT NOT NULL,
            conditions_score DOUBLE PRECISION NOT NULL,
            updated_at TEXT,
            source TEXT
        );

        CREATE TABLE IF NOT EXISTS resort_condition_history (
            id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            resort_id TEXT NOT NULL REFERENCES ski_areas(ski_area_id) ON DELETE CASCADE,
            resort_name TEXT NOT NULL,
            observed_month INTEGER NOT NULL,
            observed_at TEXT NOT NULL,
            snow_confidence_score DOUBLE PRECISION NOT NULL,
            snow_confidence_label TEXT NOT NULL,
            availability_status TEXT NOT NULL,
            weather_summary TEXT NOT NULL,
            conditions_score DOUBLE PRECISION NOT NULL,
            source TEXT,
            UNIQUE(resort_id, observed_at)
        );

        CREATE TABLE IF NOT EXISTS raw_weather_history (
            id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            resort_id TEXT NOT NULL REFERENCES ski_areas(ski_area_id) ON DELETE CASCADE,
            resort_name TEXT NOT NULL,
            elevation_band TEXT NOT NULL DEFAULT 'mid'
              CHECK (elevation_band IN ('base', 'mid', 'upper')),
            elevation_m INTEGER,
            observed_on DATE NOT NULL,
            observed_at TEXT NOT NULL,
            snowfall_cm DOUBLE PRECISION NOT NULL,
            snow_depth_m DOUBLE PRECISION,
            precipitation_sum_mm DOUBLE PRECISION,
            rain_sum_mm DOUBLE PRECISION,
            precipitation_hours DOUBLE PRECISION,
            snowfall_water_equivalent_sum_mm DOUBLE PRECISION,
            temperature_2m_max_c DOUBLE PRECISION NOT NULL,
            temperature_2m_min_c DOUBLE PRECISION NOT NULL,
            apparent_temperature_2m_max_c DOUBLE PRECISION,
            apparent_temperature_2m_min_c DOUBLE PRECISION,
            cloud_cover_mean_pct DOUBLE PRECISION,
            sunshine_duration_seconds DOUBLE PRECISION,
            visibility_min_m DOUBLE PRECISION,
            wind_speed_10m_max_kmh DOUBLE PRECISION NOT NULL,
            wind_gusts_10m_max_kmh DOUBLE PRECISION NOT NULL,
            weather_code INTEGER NOT NULL,
            record_type TEXT NOT NULL DEFAULT 'archive',
            source TEXT,
            source_model TEXT,
            CONSTRAINT raw_weather_history_resort_band_observed_source_key
            UNIQUE(resort_id, elevation_band, observed_on, source)
        );

        CREATE TABLE IF NOT EXISTS llm_parse_cache (
            cache_key TEXT PRIMARY KEY,
            query_text TEXT NOT NULL,
            model TEXT NOT NULL,
            prompt_version TEXT NOT NULL,
            schema_version TEXT NOT NULL,
            response_json TEXT NOT NULL,
            created_at TEXT NOT NULL
        );

        CREATE TABLE IF NOT EXISTS llm_narrative_cache (
            cache_key TEXT PRIMARY KEY,
            result_signature TEXT NOT NULL,
            model TEXT NOT NULL,
            prompt_version TEXT NOT NULL,
            schema_version TEXT NOT NULL,
            response_json TEXT NOT NULL,
            created_at TEXT NOT NULL
        );

        CREATE TABLE IF NOT EXISTS outbound_booking_clicks (
            id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            created_at TEXT NOT NULL,
            resort_id TEXT NOT NULL REFERENCES resorts(resort_id) ON DELETE CASCADE,
            selected_area_name TEXT NOT NULL,
            selected_ski_area_name TEXT,
            target_url TEXT NOT NULL,
            source_surface TEXT NOT NULL,
            request_id TEXT,
            user_agent TEXT
        );

        CREATE TABLE IF NOT EXISTS app_users (
            user_id TEXT PRIMARY KEY,
            auth_provider TEXT NOT NULL,
            provider_subject TEXT NOT NULL,
            email TEXT NOT NULL,
            display_name TEXT,
            created_at TEXT NOT NULL,
            updated_at TEXT NOT NULL,
            UNIQUE (auth_provider, provider_subject),
            UNIQUE (email)
        );

        CREATE TABLE IF NOT EXISTS auth_sessions (
            session_id TEXT PRIMARY KEY,
            user_id TEXT NOT NULL REFERENCES app_users(user_id) ON DELETE CASCADE,
            token_hash TEXT NOT NULL UNIQUE,
            created_at TEXT NOT NULL,
            expires_at TEXT NOT NULL,
            last_used_at TEXT NOT NULL
        );

        CREATE TABLE IF NOT EXISTS current_trip (
            singleton_id INTEGER PRIMARY KEY CHECK (singleton_id = 1),
            resort_id TEXT NOT NULL REFERENCES resorts(resort_id) ON DELETE CASCADE,
            resort_name TEXT NOT NULL,
            selected_area_name TEXT NOT NULL,
            selected_ski_area_id TEXT,
            selected_ski_area_name TEXT,
            travel_month INTEGER,
            trip_start_date DATE,
            trip_end_date DATE,
            booking_status TEXT NOT NULL,
            created_at TEXT NOT NULL,
            updated_at TEXT NOT NULL,
            last_checked_at TEXT
        );

        CREATE TABLE IF NOT EXISTS user_current_trip (
            user_id TEXT PRIMARY KEY REFERENCES app_users(user_id) ON DELETE CASCADE,
            resort_id TEXT NOT NULL REFERENCES resorts(resort_id) ON DELETE CASCADE,
            resort_name TEXT NOT NULL,
            selected_area_name TEXT NOT NULL,
            selected_ski_area_id TEXT,
            selected_ski_area_name TEXT,
            travel_month INTEGER,
            trip_start_date DATE,
            trip_end_date DATE,
            booking_status TEXT NOT NULL,
            created_at TEXT NOT NULL,
            updated_at TEXT NOT NULL,
            last_checked_at TEXT
        );

        CREATE TABLE IF NOT EXISTS user_devices (
            user_id TEXT NOT NULL REFERENCES app_users(user_id) ON DELETE CASCADE,
            installation_id TEXT NOT NULL,
            platform TEXT NOT NULL,
            push_token TEXT,
            push_enabled BOOLEAN NOT NULL DEFAULT TRUE,
            created_at TEXT NOT NULL,
            updated_at TEXT NOT NULL,
            last_seen_at TEXT NOT NULL,
            PRIMARY KEY (user_id, installation_id)
        );

        CREATE TABLE IF NOT EXISTS companion_events (
            event_id TEXT PRIMARY KEY,
            user_id TEXT NOT NULL REFERENCES app_users(user_id) ON DELETE CASCADE,
            resort_id TEXT NOT NULL REFERENCES resorts(resort_id) ON DELETE CASCADE,
            event_type TEXT NOT NULL,
            event_signature TEXT NOT NULL,
            actionable BOOLEAN NOT NULL,
            summary TEXT NOT NULL,
            changes_json TEXT NOT NULL,
            trip_window_status TEXT NOT NULL,
            conditions_updated_at TEXT,
            recorded_at TEXT NOT NULL,
            UNIQUE (user_id, event_signature)
        );
        """
    )
    connection.execute(
        """
        ALTER TABLE raw_weather_history
        ADD COLUMN IF NOT EXISTS record_type TEXT NOT NULL DEFAULT 'archive'
        """
    )
    connection.execute(
        """
        ALTER TABLE raw_weather_history
        ADD COLUMN IF NOT EXISTS elevation_band TEXT NOT NULL DEFAULT 'upper'
        """
    )
    connection.execute(
        """
        ALTER TABLE raw_weather_history
        ADD COLUMN IF NOT EXISTS elevation_m INTEGER
        """
    )
    connection.execute(
        """
        ALTER TABLE raw_weather_history
        ADD COLUMN IF NOT EXISTS precipitation_sum_mm DOUBLE PRECISION,
        ADD COLUMN IF NOT EXISTS rain_sum_mm DOUBLE PRECISION,
        ADD COLUMN IF NOT EXISTS precipitation_hours DOUBLE PRECISION,
        ADD COLUMN IF NOT EXISTS snowfall_water_equivalent_sum_mm DOUBLE PRECISION,
        ADD COLUMN IF NOT EXISTS apparent_temperature_2m_max_c DOUBLE PRECISION,
        ADD COLUMN IF NOT EXISTS apparent_temperature_2m_min_c DOUBLE PRECISION,
        ADD COLUMN IF NOT EXISTS cloud_cover_mean_pct DOUBLE PRECISION,
        ADD COLUMN IF NOT EXISTS sunshine_duration_seconds DOUBLE PRECISION,
        ADD COLUMN IF NOT EXISTS visibility_min_m DOUBLE PRECISION
        """
    )
    connection.execute(
        """
        UPDATE raw_weather_history AS history
        SET elevation_m = ski_areas.summit_elevation_m
        FROM ski_areas
        WHERE history.elevation_m IS NULL
          AND history.resort_id = ski_areas.ski_area_id
        """
    )
    connection.execute(
        """
        DO $$
        BEGIN
          IF EXISTS (
            SELECT 1
            FROM pg_constraint
            WHERE conname = 'raw_weather_history_resort_id_observed_on_source_key'
          ) THEN
            ALTER TABLE raw_weather_history
            DROP CONSTRAINT raw_weather_history_resort_id_observed_on_source_key;
          END IF;
        END $$;
        """
    )
    connection.execute(
        """
        DO $$
        BEGIN
          IF NOT EXISTS (
            SELECT 1
            FROM pg_constraint
            WHERE conname = 'raw_weather_history_elevation_band_check'
          ) THEN
            ALTER TABLE raw_weather_history
            ADD CONSTRAINT raw_weather_history_elevation_band_check
            CHECK (elevation_band IN ('base', 'mid', 'upper'));
          END IF;
        END $$;
        """
    )
    connection.execute(
        """
        DO $$
        BEGIN
          IF NOT EXISTS (
            SELECT 1
            FROM pg_constraint
            WHERE conname = 'raw_weather_history_resort_band_observed_source_key'
          ) THEN
            ALTER TABLE raw_weather_history
            ADD CONSTRAINT raw_weather_history_resort_band_observed_source_key
            UNIQUE (resort_id, elevation_band, observed_on, source);
          END IF;
        END $$;
        """
    )
    connection.execute(
        """
        ALTER TABLE current_trip
        ADD COLUMN IF NOT EXISTS trip_start_date DATE
        """
    )
    connection.execute(
        """
        ALTER TABLE current_trip
        ADD COLUMN IF NOT EXISTS trip_end_date DATE
        """
    )
    connection.execute(
        """
        ALTER TABLE user_current_trip
        ADD COLUMN IF NOT EXISTS trip_start_date DATE
        """
    )
    connection.execute(
        """
        ALTER TABLE user_current_trip
        ADD COLUMN IF NOT EXISTS trip_end_date DATE
        """
    )


def _sync_resorts_from_seed(
    connection: psycopg.Connection[Any],
    resorts_path: Path,
) -> None:
    resorts = load_resorts_from_path(resorts_path)
    seeded_ids = sorted(resort.resort_id for resort in resorts)

    if seeded_ids:
        connection.execute(
            "DELETE FROM resorts WHERE NOT (resort_id = ANY(%s))",
            (seeded_ids,),
        )
    else:
        connection.execute("DELETE FROM resorts")

    for resort in resorts:
        connection.execute(
            """
            INSERT INTO resorts (
                resort_id,
                name,
                country,
                region,
                price_level,
                latitude,
                longitude,
                base_elevation_m,
                summit_elevation_m,
                season_start_month,
                season_end_month
            )
            VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s)
            ON CONFLICT (resort_id) DO UPDATE SET
                name = excluded.name,
                country = excluded.country,
                region = excluded.region,
                price_level = excluded.price_level,
                latitude = excluded.latitude,
                longitude = excluded.longitude,
                base_elevation_m = excluded.base_elevation_m,
                summit_elevation_m = excluded.summit_elevation_m,
                season_start_month = excluded.season_start_month,
                season_end_month = excluded.season_end_month
            """,
            (
                resort.resort_id,
                resort.name,
                resort.country,
                resort.region,
                resort.price_level,
                resort.latitude,
                resort.longitude,
                resort.base_elevation_m,
                resort.summit_elevation_m,
                resort.season_start_month,
                resort.season_end_month,
            ),
        )

        connection.execute(
            """
            DELETE FROM stay_base_skill_levels
            WHERE stay_base_id IN (
                SELECT id FROM stay_bases WHERE resort_id = %s
            )
            """,
            (resort.resort_id,),
        )
        connection.execute(
            "DELETE FROM stay_bases WHERE resort_id = %s",
            (resort.resort_id,),
        )
        connection.execute(
            "DELETE FROM rentals WHERE resort_id = %s",
            (resort.resort_id,),
        )

        current_ski_area_ids = [ski_area.ski_area_id for ski_area in resort.ski_areas]
        for ski_area in resort.ski_areas:
            connection.execute(
                """
                INSERT INTO ski_areas (
                    resort_id,
                    ski_area_id,
                    name,
                    latitude,
                    longitude,
                    base_elevation_m,
                    summit_elevation_m,
                    season_start_month,
                    season_end_month
                ) VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s)
                ON CONFLICT (ski_area_id) DO UPDATE SET
                    resort_id = excluded.resort_id,
                    name = excluded.name,
                    latitude = excluded.latitude,
                    longitude = excluded.longitude,
                    base_elevation_m = excluded.base_elevation_m,
                    summit_elevation_m = excluded.summit_elevation_m,
                    season_start_month = excluded.season_start_month,
                    season_end_month = excluded.season_end_month
                """,
                (
                    resort.resort_id,
                    ski_area.ski_area_id,
                    ski_area.name,
                    ski_area.latitude,
                    ski_area.longitude,
                    ski_area.base_elevation_m,
                    ski_area.summit_elevation_m,
                    ski_area.season_start_month,
                    ski_area.season_end_month,
                ),
            )

        if current_ski_area_ids:
            connection.execute(
                """
                DELETE FROM ski_areas
                WHERE resort_id = %s AND NOT (ski_area_id = ANY(%s))
                """,
                (resort.resort_id, current_ski_area_ids),
            )
        else:
            connection.execute(
                "DELETE FROM ski_areas WHERE resort_id = %s",
                (resort.resort_id,),
            )

        for stay_base in resort.stay_bases:
            stay_base_row = connection.execute(
                """
                INSERT INTO stay_bases (
                    resort_id,
                    name,
                    price_range,
                    price_min,
                    price_max,
                    quality,
                    lift_distance
                ) VALUES (%s, %s, %s, %s, %s, %s, %s)
                RETURNING id
                """,
                (
                    resort.resort_id,
                    stay_base.name,
                    stay_base.price_range,
                    stay_base.price_min,
                    stay_base.price_max,
                    stay_base.quality,
                    stay_base.lift_distance,
                ),
            ).fetchone()
            assert stay_base_row is not None
            for skill_level in stay_base.supported_skill_levels:
                connection.execute(
                    """
                    INSERT INTO stay_base_skill_levels (stay_base_id, skill_level)
                    VALUES (%s, %s)
                    """,
                    (stay_base_row["id"], skill_level),
                )

        for rental in resort.rentals:
            connection.execute(
                """
                INSERT INTO rentals (
                    resort_id,
                    name,
                    price_range,
                    price_min,
                    price_max,
                    quality,
                    lift_distance
                ) VALUES (%s, %s, %s, %s, %s, %s, %s)
                """,
                (
                    resort.resort_id,
                    rental.name,
                    rental.price_range,
                    rental.price_min,
                    rental.price_max,
                    rental.quality,
                    rental.lift_distance,
                ),
            )


def _clear_legacy_seeded_conditions(connection: psycopg.Connection[Any]) -> None:
    connection.execute("DELETE FROM resort_conditions WHERE source IS NULL")
